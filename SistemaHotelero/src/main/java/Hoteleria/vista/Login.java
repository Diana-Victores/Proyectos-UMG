    private void txt_logearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txt_logearActionPerformed
    if (txt_usuario.getText().trim().isEmpty() || txt_contraseña.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "NO PUEDEN HABER CAMPOS VACIOS", "ERROR", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                seguridad.dominio.Usuario usuarioAConsultar = new seguridad.dominio.Usuario();
                UsuarioDAO usuarioDAO = new UsuarioDAO();
                usuarioAConsultar.setUser_usuario((txt_usuario.getText()));
                usuarioAConsultar = usuarioDAO.query(usuarioAConsultar);
                
                if (txt_contraseña.getText().equals(usuarioAConsultar.getPassword_usuario()) && 
                    txt_usuario.getText().equals(usuarioAConsultar.getUser_usuario())) {
                    //SE BUSCA EN LAS TABLAS USUARIO-APLICACIÓN Y USUARIO;
                    PermisosDAO permisos = new PermisosDAO();
                    Permisos permi = new Permisos();
                    
                    permi.ejecutarBusqueda(usuarioAConsultar.getUser_usuario());
                    permisos.setNombreUsuario(usuarioAConsultar.getUser_usuario());
                    permi.getAplicaciones();
                    usuario=usuarioAConsultar.getNombre_usuario()+" "+usuarioAConsultar.getApellido_usuario();
                    LoginMDI.validarids(usuarioAConsultar.getId_usuario());
                    this.dispose();
                    //--PREGUNTAR SI HACE FALTA ESTE CODIGO O ELIMINARLO.
                   /* Asignacion_Aplicacion_UsuarioDAO dao = new Asignacion_Aplicacion_UsuarioDAO();
                    List<Asignacion_Aplicacion_Usuario> personas = dao.select();
                    for (Asignacion_Aplicacion_Usuario persona : personas) {
                        if (permisos.getCodigoUsuario() == Integer.parseInt(persona.getCodigo_Usuario())) {

                            permisos.getPermisosApp(Integer.parseInt(persona.getCodigo_Aplicacion()));
                            System.out.println("CODIGO: "+permisos.getCodigoUsuario()+" CANTIDAD DE APPS: "
                                               +permisos.getCantidadApps());
                        }                        
                    }*/
                    
                } else {
                    JOptionPane.showMessageDialog(this, "ERROR AL ENCONTRAR USUARIO O CONTRASEÑA", "ERROR", JOptionPane.ERROR_MESSAGE);
                    txt_contraseña.setText("");txt_usuario.setText("");
                }
            } catch (HeadlessException e) {
                JOptionPane.showMessageDialog(this, "ERROR AL ENCONTRAR USUARIO O CONTRASEÑA", "ERROR", JOptionPane.ERROR_MESSAGE);
                txt_contraseña.setText("");txt_usuario.setText("");
            } catch (UnknownHostException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
        }

    }//GEN-LAST:event_txt_logearActionPerformed

