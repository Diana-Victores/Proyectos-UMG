     public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID Articulo");
        modelo.addColumn("Fecha Ingreso");
        modelo.addColumn("Nombre Articulo");
        modelo.addColumn("Talla XS");
        modelo.addColumn("Talla S");
        modelo.addColumn("Talla M");
        modelo.addColumn("Talla L");
        modelo.addColumn("Talla XL");
        modelo.addColumn("Color Articulo");
        modelo.addColumn("Nombre Proveedor");
        modelo.addColumn("Existencias");

        RegistroArticuloDAO registroarcticuloDAO = new RegistroArticuloDAO();

        List<RegistroArticulo> registroarticulos = registroarcticuloDAO.select();
        TablaArticulo.setModel(modelo);
        String[] dato = new String[11];
        for (int i = 0; i < registroarticulos.size(); i++) {
            dato[0] = Integer.toString(registroarticulos.get(i).getPK_id_articulo());
            dato[1] = registroarticulos.get(i).getFecha_ingreso();
            dato[2] = registroarticulos.get(i).getNombre_articulo();
            dato[3] = registroarticulos.get(i).getTalla_articuloXS();
            dato[4] = registroarticulos.get(i).getTalla_articuloS();
            dato[5] = registroarticulos.get(i).getTalla_articuloM();
            dato[6] = registroarticulos.get(i).getTalla_articuloL();
            dato[7] = registroarticulos.get(i).getTalla_articuloXL();
            dato[8] = registroarticulos.get(i).getColor_articulo();
            dato[9] = registroarticulos.get(i).getNombre_proveedor();
            dato[10] = registroarticulos.get(i).getExistencia_articulo();

            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

