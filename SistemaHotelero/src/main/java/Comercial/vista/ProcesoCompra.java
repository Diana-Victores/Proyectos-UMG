private void mostrarListado(Vector lista) {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("IDCOMPRA");
        modelo.addColumn("IDPROVEEDOR");
        modelo.addColumn("NOMBRE");
        modelo.addColumn("NIT");
        modelo.addColumn("IDPRODUCTO");
        modelo.addColumn("NOMBREPRODUCTO");
        modelo.addColumn("CANTIDAD");
        modelo.addColumn("PRECIO");
        modelo.addColumn("TOTAL");
        Object[] datos = new Object[9];
        Producto obj;
        float sum = 0;
        for (int i = 0; i < lista.size(); i++) {
            obj = (Producto) lista.elementAt(i);
//String Inicio = new SimpleDateFormat("dd/MM/yyyy").format(txt_fecha.getDate());
            datos[0] = txt_idcompras.getText();
            datos[1] = txt_combox.getSelectedItem().toString();
            datos[2] = txt_nombre.getText();
            datos[3] = txt_nit.getText();
            datos[4] = uno.getSelectedItem().toString();
            datos[5] = txt_nombreproducto.getText();
            datos[6] = obj.getCantidad();
            datos[7] = obj.getPrecio();
            datos[8] = obj.hallarGanancia();
            modelo.addRow(datos);
            sum = sum + obj.hallarGanancia();
        }
        tbtTabla.setModel(modelo);

        txt_total.setText(String.valueOf(sum));
    }

