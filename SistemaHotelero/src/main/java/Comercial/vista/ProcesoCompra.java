    private void btnEliminarUnProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarUnProductoActionPerformed
        if (tbtTabla.getSelectedRow() >= 0) {
            if (tbtTabla.getValueAt(tbtTabla.getSelectedRow(), 0) == null) {
                JOptionPane.showMessageDialog(this, "LA FILA QUE SELECCIONO ,NO CUENTA CON NINGUN PRODUCTO", "SISTEMA", JOptionPane.WARNING_MESSAGE);
            } else {
                DefaultTableModel tm = (DefaultTableModel) tbtTabla.getModel();
                int rpta = JOptionPane.showConfirmDialog(this, "ESTA SEGURO DE ELIMINAR ESTA FILA", "SISTEMA", JOptionPane.INFORMATION_MESSAGE);
                if (rpta == JOptionPane.YES_OPTION) {
                    tm.removeRow(tbtTabla.getSelectedRow());
                    Producto obj = new Producto();
                    /*llamamos a la funcion obj.eliminar , para que se borre de nuestra lista*/
                    obj.eliminar();
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "DEBE SELECCIONAR UNA FILA", "SISTEMA", JOptionPane.WARNING_MESSAGE);

        }
    }//GEN-LAST:event_btnEliminarUnProductoActionPerformed

    private void btnEliminarTodoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarTodoActionPerformed

        try {
            int rpta = JOptionPane.showConfirmDialog(this, "ESTA SEGURO DE  BORRAR TODOS LOS PRODUCTO REGISTRADOS", "SISTEMA", JOptionPane.INFORMATION_MESSAGE);
            if (rpta == JOptionPane.YES_OPTION) {
                Producto obj = new Producto();
                DefaultTableModel tm = (DefaultTableModel) tbtTabla.getModel();
                for (int i = tbtTabla.getRowCount() - 1; i >= 0; i--) {
                    tm.removeRow(i);
                    obj.eliminar();

                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "ERROR");
        }

    }//GEN-LAST:event_btnEliminarTodoActionPerformed
 private void agregar() {

        if (txt_cantidad.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "INGRESE CANTIDA A COMPRAR", "SISTEMA", JOptionPane.WARNING_MESSAGE);
            txt_cantidad.requestFocus();
            return;
        }
        if (txt_precio.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "INGRESE EL PRECIO DEL PRODUCTO", "SISTEMA", JOptionPane.WARNING_MESSAGE);
            txt_precio.requestFocus();
            return;
        }
        //Entrada

        int cantidad = Integer.parseInt(txt_cantidad.getText());
        int precio = Integer.parseInt(txt_precio.getText());
        //Proceso
        Producto obj = new Producto();

        obj.setCantidad(cantidad);
        obj.setPrecio(precio);
        obj.agregar();
        //Salida
        JOptionPane.showMessageDialog(this, "COMPRA REALIZADA", "SISTEMA", JOptionPane.INFORMATION_MESSAGE);
        Vector lista = obj.mostrar();
        mostrarListado(lista);
        limpiar();
    }

